    private void withdrawActionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawActionBtnActionPerformed
        // TODO add your handling code here:
        if(Double.parseDouble(withdrawAmt.getText()) > balance) {
            JOptionPane.showMessageDialog(null, 
                              "You do not have enough money to withdraw!", 
                              "ERROR", 
                              JOptionPane.WARNING_MESSAGE);
        }
        else {
            String text = withdrawAmt.getText();
            double value = Double.parseDouble(text);
            balance -= value;
        }
        withdrawAmt.setText(null);
    }//GEN-LAST:event_withdrawActionBtnActionPerformed

