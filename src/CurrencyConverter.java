    private void depositActionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositActionBtnActionPerformed
        // TODO add your handling code here:
        if(depositAmt.getText() == null || Double.parseDouble(depositAmt.getText()) <= 0) {
            JOptionPane.showMessageDialog(null, 
                              "Invalid Entry!", 
                              "ERROR", 
                              JOptionPane.WARNING_MESSAGE);
        }
        else {
            String text = depositAmt.getText();
            double value = Double.parseDouble(text);
            String currencyType = currencyTypeList.getSelectedItem().toString();
            double convertedAmount = convertCurrency(value, currencyType);
            balance += convertedAmount;
        }
        depositAmt.setText(null);
    }//GEN-LAST:event_depositActionBtnActionPerformed

