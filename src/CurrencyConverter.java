    public static double convertCurrency(double amount, String type) {
        double finalAmt = 0;
        
        switch(type) {
            case "U.S Dollars": {
                amount /= 1;
                break;
            }
            case "Euros": {
                amount /= 0.89;
                break;
            }
            case "British Pound": {
                amount /= 0.78;
                break;
            }
        }
        finalAmt = amount;
        return finalAmt;
    }
    
